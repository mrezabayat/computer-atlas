#!/usr/bin/env node
/**
 * Generate a lightweight progress-metadata module for the on-demand worker.
 *
 * The /api/progress and /api/progress/me endpoints (prerender = false) only need
 * each path's title + topic IDs (and which count toward completion) and each
 * topic's title. Importing astro:content (getCollection) in those routes would
 * otherwise drag the entire ~2.8 MiB content data store into the Cloudflare
 * Worker bundle and blow past the 3 MiB size limit. This script extracts just
 * that metadata from frontmatter and emits src/lib/progress-meta.generated.ts.
 *
 * Runs automatically before `astro build`, `astro dev`, and `astro check`
 * (see package.json), or on demand via `npm run gen:progress-meta`.
 *
 * Topic / path IDs mirror content.config.ts: the basename without extension.
 * The topics[] shape mirrors lib/paths.ts normalisePathTopics (bare slug, or
 * an object { ref, optional?, section? }).
 */

import { readFile, writeFile } from "node:fs/promises";
import { globSync } from "node:fs";
import { basename } from "node:path";
import { parse as parseYaml } from "yaml";

const OUT = "src/lib/progress-meta.generated.ts";

const slug = (file) => basename(file).replace(/\.mdx?$/, "");

function frontmatter(src) {
  const m = src.match(/^---\n([\s\S]*?)\n---/);
  return m ? parseYaml(m[1]) : null;
}

/** Mirror lib/paths.ts: bare slug or { ref, optional? } → all + required IDs. */
function normaliseTopics(topics) {
  const topicIds = [];
  const requiredTopicIds = [];
  for (const entry of topics ?? []) {
    const id = typeof entry === "string" ? entry : entry.ref;
    const optional = typeof entry === "string" ? false : entry.optional ?? false;
    if (!id) continue;
    topicIds.push(id);
    if (!optional) requiredTopicIds.push(id);
  }
  return { topicIds, requiredTopicIds };
}

async function main() {
  const topicFiles = globSync("src/content/topics/**/*.mdx").sort();
  const pathFiles = globSync("src/content/paths/**/*.mdx").sort();

  const topicTitles = {};
  // Lightweight per-topic metadata for the on-demand newsletter digest route
  // (prerender = false), which must not import astro:content. updated is an ISO
  // string so it serializes cleanly and round-trips via new Date().
  const topicsMeta = [];
  for (const file of topicFiles) {
    const fm = frontmatter(await readFile(file, "utf8"));
    if (!fm?.title) continue;
    const id = slug(file);
    topicTitles[id] = fm.title;
    topicsMeta.push({
      id,
      title: fm.title,
      summary: fm.summary ?? "",
      updated: fm.updated ? new Date(fm.updated).toISOString() : null,
    });
  }

  const pathsMeta = [];
  for (const file of pathFiles) {
    const fm = frontmatter(await readFile(file, "utf8"));
    if (!fm) continue;
    pathsMeta.push({
      id: slug(file),
      title: fm.title,
      ...normaliseTopics(fm.topics),
    });
  }

  const out =
    `// AUTO-GENERATED by scripts/gen-progress-meta.mjs — do not edit by hand.\n` +
    `// Regenerate with \`npm run gen:progress-meta\` (runs before build/dev/check).\n\n` +
    `export interface ProgressPathMeta {\n` +
    `  id: string;\n` +
    `  title: string;\n` +
    `  /** All topic slugs in path order (optional + required). */\n` +
    `  topicIds: string[];\n` +
    `  /** Topic slugs that count toward completion (optional excluded). */\n` +
    `  requiredTopicIds: string[];\n` +
    `}\n\n` +
    `export interface TopicMeta {\n` +
    `  id: string;\n` +
    `  title: string;\n` +
    `  summary: string;\n` +
    `  /** ISO timestamp, or null if the topic has no \`updated\` date. */\n` +
    `  updated: string | null;\n` +
    `}\n\n` +
    `export const topicTitles: Record<string, string> = ${JSON.stringify(topicTitles, null, 2)};\n\n` +
    `export const topicsMeta: TopicMeta[] = ${JSON.stringify(topicsMeta, null, 2)};\n\n` +
    `export const pathsMeta: ProgressPathMeta[] = ${JSON.stringify(pathsMeta, null, 2)};\n`;

  await writeFile(OUT, out);
  console.log(
    `gen-progress-meta OK → ${pathsMeta.length} paths, ${Object.keys(topicTitles).length} topic titles, ${topicsMeta.length} topics meta → ${OUT}`,
  );
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
